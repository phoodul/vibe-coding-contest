/**
 * 학생 입력 텍스트에서 영문법 도메인인지 1차 분류 (간단 휴리스틱).
 * Phase 1 임베딩 검색 통합 전 임시.
 */
export function looksLikeEnglishGrammarQuestion(text: string): boolean {
  if (!text) return false;
  // 영어 문장 또는 영문법 한국어 키워드
  const englishCharRatio =
    text.replace(/[^a-zA-Z]/g, '').length / Math.max(text.length, 1);
  if (englishCharRatio > 0.3) return true;
  const koGrammarKeywords = [
    '시제', '관계대명사', '가정법', '수동태', '분사', '동명사',
    '관계부사', '도치', '강조', '동격', '평행',
    '현재완료', '과거완료', 'whose', 'whom', 'which', 'that',
  ];
  return koGrammarKeywords.some((k) => text.includes(k));
}
