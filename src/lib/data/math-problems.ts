// 문제 번호 매핑: 2017~2021은 1~30, 2022~2026은 공통 1~22, 선택 23~30
function getQuestionNumber(type: string, index: number): number {
  if (type === "확률과통계" || type === "미적분" || type === "기하") {
    return 23 + index; // 선택과목은 23번부터
  }
  return index + 1;
}

function isMultipleChoice(type: string, index: number): boolean {
  if (type === "가형" || type === "나형") {
    return index < 21; // 1~21번 객관식, 22~30번 주관식
  }
  if (type === "공통") {
    return index < 15; // 1~15번 객관식, 16~22번 주관식
  }
  // 선택과목: 23~28번 객관식, 29~30번 주관식
  return index < 6;
}

// 전체 문제 생성
export const mathProblems: MathProblem[] = RAW_ANSWERS.flatMap((exam) =>
  exam.answers.map((answer, i) => ({
    id: `${exam.year}-${exam.type}-${getQuestionNumber(exam.type, i)}`,
    year: exam.year,
    type: exam.type,
    number: getQuestionNumber(exam.type, i),
    answer,
    isMultipleChoice: isMultipleChoice(exam.type, i),
  }))
);

// 필터 유틸
export function filterProblems(opts: {
  year?: number;
  type?: string;
}): MathProblem[] {
  return mathProblems.filter((p) => {
    if (opts.year && p.year !== opts.year) return false;
    if (opts.type && p.type !== opts.type) return false;
    return true;
  });
}

export function getProblemById(id: string): MathProblem | undefined {
  return mathProblems.find((p) => p.id === id);
}

